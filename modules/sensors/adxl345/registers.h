#ifndef __ADXL345_REGISTERS
#define __ADXL345_REGISTERS

// ADXL345 Registers
typedef enum {
  ADXL345_REGISTER_DEVID          = 0x00,
  ADXL345_REGISTER_THRESH_TAP     = 0x1D,
  ADXL345_REGISTER_OFSX           = 0x1E,
  ADXL345_REGISTER_OFSY           = 0x1F,
  ADXL345_REGISTER_OFSZ           = 0x20,
  ADXL345_REGISTER_DUR            = 0x21,
  ADXL345_REGISTER_LATENT         = 0x22,
  ADXL345_REGISTER_WINDOW         = 0x23,
  ADXL345_REGISTER_THRESH_ACT     = 0x24,
  ADXL345_REGISTER_THRESH_INACT   = 0x25,
  ADXL345_REGISTER_TIME_INACT     = 0x26,
  ADXL345_REGISTER_ACT_INACT_CTL  = 0x27,
  ADXL345_REGISTER_THRESH_FF      = 0x28,
  ADXL345_REGISTER_TIME_FF        = 0x29,
  ADXL345_REGISTER_TAP_AXES       = 0x2A,
  ADXL345_REGISTER_ACT_TAP_STATUS = 0x2B,
  ADXL345_REGISTER_BW_RATE        = 0x2C,
  ADXL345_REGISTER_POWER_CTL      = 0x2D,
  ADXL345_REGISTER_INT_ENABLE     = 0x2E,
  ADXL345_REGISTER_INT_MAP        = 0x2F,
  ADXL345_REGISTER_INT_SOURCE     = 0x30,
  ADXL345_REGISTER_DATA_FORMAT    = 0x31,
  ADXL345_REGISTER_DATA_X0        = 0x32,
  ADXL345_REGISTER_DATA_X1        = 0x33,
  ADXL345_REGISTER_DATA_Y0        = 0x34,
  ADXL345_REGISTER_DATA_Y1        = 0x35,
  ADXL345_REGISTER_DATA_Z0        = 0x36,
  ADXL345_REGISTER_DATA_Z1        = 0x37,
  ADXL345_REGISTER_FIFO_CTL       = 0x38,
  ADXL345_REGISTER_FIFO_STATUS    = 0x39
} adxl345_register_t;

typedef enum {
  ADXL345_POWER_WAKEUP    = 0x03,
  ADXL345_POWER_SLEEP     = 0x04,
  ADXL345_POWER_MEASURE   = 0x08,
  ADXL345_POWER_AUTOSLEEP = 0xA,
  ADXL345_POWER_LINK      = 0x20
} adxl345_power_t;

typedef enum {
  ADXL345_RANGE_2G   = 0,
  ADXL345_RANGE_4G   = 1,
  ADXL345_RANGE_8G   = 2,
  ADXL345_RANGE_16G  = 3
} adxl345_range_t;

typedef enum {
  ADXL345_INTERRUPT_OVERRUN    = 0x01,
  ADXL345_INTERRUPT_WATERMARK  = 0x02,
  ADXL345_INTERRUPT_FREE_FALL  = 0x04,
  ADXL345_INTERRUPT_INACTIVITY = 0x08,
  ADXL345_INTERRUPT_ACTIVITY   = 0xA,
  ADXL345_INTERRUPT_DOUBLE_TAP = 0x20,
  ADXL345_INTERRUPT_SINGLE_TAP = 0x40,
  ADXL345_INTERRUPT_DATA_READY  = 0x80
} adxl345_interrupt_t;

typedef enum {
  ADXL345_TAP_AXES_Z = 1,
  ADXL345_TAP_AXES_Y = 2,
  ADXL345_TAP_AXES_X = 4
} adxl345_tap_axes_t;

#define ADXL345_DEVID_CODE 0xE5
#define ADXL345_INTERRUPT_TAP 0x40

#define ADXL345_DATA_FORMAT_RANGE_BIT 0
#define ADXL345_DATA_FORMAT_FULL_RES_BIT 3
#define ADXL345_RATE_BIT 0

#endif
